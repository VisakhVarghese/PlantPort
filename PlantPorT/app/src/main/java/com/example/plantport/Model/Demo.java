package com.example.plantport.Model;

public class Demo {

    int Image;
    String Name;

    public Demo() {
    }

    public Demo(int image, String name) {
        Image = image;
        Name = name;
    }

    public int getImage() {
        return Image;
    }

    public void setImage(int image) {
        Image = image;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
